/**
 * Returns the current date in EST (America/New_York) formatted as YYYY-MM-DD.
 */
export function getESTDate(date?: Date | string): string {
  const d = date ? (typeof date === 'string' ? new Date(date + (date.includes("T") ? "" : "T00:00:00")) : date) : new Date();
  return d.toLocaleDateString('en-CA', { timeZone: 'America/New_York' });
}

/**
 * Normalizes a name by stripping leading @ and checking against mappings.
 */
export function normalizeName(name: string): string {
  if (!name) return "";
  let trimmed = name.trim();
  
  // Standardize case-insensitive and handle @ signs
  if (trimmed.startsWith('@')) {
    trimmed = trimmed.substring(1).trim();
  }

  // Check mapping (case-sensitive check first for speed)
  if (NAME_MAPPINGS[trimmed]) return NAME_MAPPINGS[trimmed];

  // Case-insensitive fallback
  const lowerTrimmed = trimmed.toLowerCase();
  for (const [nickname, fullName] of Object.entries(NAME_MAPPINGS)) {
    if (nickname.toLowerCase() === lowerTrimmed) return fullName;
  }

  return trimmed;
}

/**
 * Finds the nickname associated with a full name by reverse-searching NAME_MAPPINGS.
 */
export function getNickname(fullName: string): string | undefined {
  if (!fullName) return undefined;
  
  // Find the first nickname that maps to this full name
  for (const [nickname, official] of Object.entries(NAME_MAPPINGS)) {
    if (official.toLowerCase() === fullName.toLowerCase()) {
      return nickname;
    }
  }
  return undefined;
}

/**
 * Finds the Monday of the week for a given date, standardized to YYYY-MM-DD.
 */
export function getMonday(dateStrOrDate: string | Date | undefined): string | null {
  if (!dateStrOrDate) return null;
  const d = typeof dateStrOrDate === 'string' 
    ? new Date(dateStrOrDate + (dateStrOrDate.includes("T") ? "" : "T00:00:00")) 
    : new Date(dateStrOrDate);
  
  if (isNaN(d.getTime())) return null;

  const day = d.getDay();
  const diff = d.getDate() - day + (day === 0 ? -6 : 1);
  const mon = new Date(d.getFullYear(), d.getMonth(), diff);
  return getESTDate(mon);
}

export function mapRating(ratingStr: string) {
  const r = (ratingStr || "").toLowerCase();
  if (r.includes('exceed')) return 100;
  if (r.includes('meets')) return 85;
  if (r.includes('needs')) return 60;
  return 80;
}

